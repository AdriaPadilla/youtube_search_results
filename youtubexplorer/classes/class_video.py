class Video():
	def __init__(self,
		origin, 
		type,
		videoId,
		kind,
		channelId,
		channelTitle,
		description,
		liveBroadcastContent,
		publishedAt,
		title,
		):
		self.origin = origin
		self.type = type
		self.videoId = videoId
		self.kind = kind
		self.channelId = channelId
		self.channelTitle = channelTitle
		self.description = description
		self.liveBroadcastContent = liveBroadcastContent
		self.publishedAt = publishedAt
		self.title = title